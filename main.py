user_progress = {}

# –§—É–Ω–∫—Ü–∏—è —Å–æ–∑–¥–∞–Ω–∏—è –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
def get_keyboard(options):
    return ReplyKeyboardMarkup(
        keyboard=[[KeyboardButton(text=option)] for option in options],
        resize_keyboard=True
    )

@dp.message(Command("start"))
async def start_quiz(message: types.Message):
    user_progress[message.from_user.id] = 0
    await ask_question(message.from_user.id, message.chat.id)

async def ask_question(user_id, chat_id):
    question_num = user_progress[user_id]
    if question_num >= len(questions):  
        meme = FSInputFile(MEME_PATH)  # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –º–µ–º
        await bot.send_photo(chat_id, meme, caption="–ù–∞ —á—Ç–æ —Ç—ã –ø–æ—Ç—Ä–∞—Ç–∏–ª —ç—Ç–∏ —á–∞—Å—ã? üòÇ")
        return
    
    question, options, _ = questions[question_num]
    await bot.send_message(chat_id, f"–í–æ–ø—Ä–æ—Å {question_num + 1}: {question}", reply_markup=get_keyboard(options))

@dp.message()
async def handle_answer(message: types.Message):
    user_id = message.from_user.id

    if user_id not in user_progress:
        await start_quiz(message)
        return

    question_num = user_progress[user_id]
    correct_answer = questions[question_num][2]

    if message.text == correct_answer:
        user_progress[user_id] += 1
        if user_progress[user_id] < len(questions):
            await ask_question(user_id, message.chat.id)
        else:
            meme = FSInputFile(MEME_PATH)
            await bot.send_photo(message.chat.id, meme, caption="–ù–∞ —á—Ç–æ —Ç—ã –ø–æ—Ç—Ä–∞—Ç–∏–ª —ç—Ç–∏ —á–∞—Å—ã? üòÇ")
            del user_progress[user_id]
    else:
        await bot.send_message(message.chat.id, "–ù–µ–≤–µ—Ä–Ω–æ! –ù–∞—á–∏–Ω–∞–µ–º –∑–∞–Ω–æ–≤–æ!")
        user_progress[user_id] = 0
        await asyncio.sleep(2)
        await ask_question(user_id, message.chat.id)

